package com.wildcodeschool.doctorwho2;

public class DoctorDetail {

	private int number;
	private String name;

	public DoctorDetail(int number, String name) {
		super();
		this.number = number;
		this.name = name;
	}

	public int getNumber() {
		return number;
	}

	public String getName() {
		return name;
	}

}
